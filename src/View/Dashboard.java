public class Dashboard {
    private boolean GreenLight;
    private boolean RedLight;
    private boolean OrangeLight;
    public Dashboard()
    {
        GreenLight = false;
        RedLight = false;
        OrangeLight = false;
    }
    public void setGreenLight(boolean set)
    {
        GreenLight = set;
    }
    public void setRedLight(boolean set)
    {
        GreenLight = set;
    }
    public void setOrangeLight(boolean set)
    {
        GreenLight = set;
    }
}
